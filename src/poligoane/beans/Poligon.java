package poligoane.beans;

public class Poligon {

	private String tipPoligon;
	private String numeFisier;
	private String filiala;
	private String tonaj;
	private String nume;

	public String getTipPoligon() {
		return tipPoligon;
	}

	public void setTipPoligon(String tipPoligon) {
		this.tipPoligon = tipPoligon;
	}

	public String getNumeFisier() {
		return numeFisier;
	}

	public void setNumeFisier(String numeFisier) {
		this.numeFisier = numeFisier;
	}

	public String getFiliala() {
		return filiala;
	}

	public void setFiliala(String filiala) {
		this.filiala = filiala;
	}

	public String getTonaj() {
		return tonaj;
	}

	public void setTonaj(String tonaj) {
		this.tonaj = tonaj;
	}

	public String getNume() {
		return nume;
	}

	public void setNume(String nume) {
		this.nume = nume;
	}

}
